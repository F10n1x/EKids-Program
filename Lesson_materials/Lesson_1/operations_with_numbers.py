# Операции с числами

# '+' - оператор сложения
a = 1 + 2
# '-' - оператор вычитания
b = 3 - 1
# '*' - оператор умножения
c = 2 * 3
# '//' - оператор целочисленного деления
e = 5 // 2
# '/' - оператор деления
d = 5 / 2
# '**' - оператор возведения в степень
f = 2 ** 2
# '%' - оператор взятия остатка от деления
g = 231 % 10

# Приоритет операций такой же, как мы привыкли.
# Чтобы поменять приоритет операций, нужно использовать скобки
h = (1 + 2) / 3
